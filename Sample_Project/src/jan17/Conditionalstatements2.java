package jan17;

public class Conditionalstatements2 {

	public static void main(String[] args) {
		int i=3;
	switch (i) {
	case 1:
		System.out.println("This is the first statement");
		
		break;
	case 2:
		System.out.println("This is the second statement");
		break;
	case 3:
		System.out.println("This is the third statement");
		break;

	default:
		System.out.println("Thsis the default statement");
		break;
	}

	}

}
